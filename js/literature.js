// 渲染故事卡片
function renderStories(stories) {
    const container = document.getElementById('stories-container');
    if (!container) return;
    
    if (stories.length === 0) {
        container.innerHTML = `
            <div class="no-results-modern">
                <i class="fas fa-book"></i>
                <p>Nada atxa ku es filtru</p>
                <button onclick="clearFilters()" class="btn-primary-modern">Limpa filtru</button>
            </div>
        `;
        return;
    }
    
    container.innerHTML = stories.map(story => `
        <article class="story-card-modern" onclick="viewStory(${story.id})">
            <div class="story-image-modern">
                <img src="${story.image}" alt="${story.title}" loading="lazy">
                <span class="story-genre-badge">${getGenreLabel(story.genre)}</span>
            </div>
            <div class="story-content-modern">
                <h3 class="story-title-modern">${story.title}</h3>
                <p class="story-original-title">${story.originalTitle}</p>
                <p class="story-author-modern"><i class="fas fa-user-edit"></i> ${story.author}</p>
                <p class="story-summary-modern">${story.summary}</p>
                <div class="story-meta-modern">
                    <span><i class="fas fa-clock"></i> ${story.readTime}</span>
                    <span><i class="fas fa-eye"></i> ${formatNumber(story.views)}</span>
                    <span><i class="fas fa-heart"></i> ${formatNumber(story.likes)}</span>
                </div>
            </div>
        </article>
    `).join('');
    
    // 更新计数
    const countEl = document.getElementById('results-count');
    if (countEl) {
        countEl.textContent = stories.length + ' kontu';
    }
}

// 获取分类标签
function getGenreLabel(genre) {
    const labels = {
        'humor': 'Humor',
        'romance': 'Romansi',
        'adventure': 'Aventura',
        'wisdom': 'Sabedoria',
        'folklore': 'Folklore'
    };
    return labels[genre] || genre;
}

// 格式化数字
function formatNumber(num) {
    if (num >= 1000) {
        return (num / 1000).toFixed(1) + 'k';
    }
    return num;
}

// 查看故事详情
function viewStory(id) {
    const story = storiesData.find(s => s.id === id);
    if (!story) return;
    
    // 存储当前故事到 sessionStorage
    sessionStorage.setItem('currentStory', JSON.stringify(story));
    
    // 跳转到详情页
    window.location.href = `story-detail.html?id=${id}`;
}

// 应用筛选
function applyFilters() {
    const genre = document.getElementById('filter-genre')?.value;
    const length = document.getElementById('filter-length')?.value;
    
    let filtered = storiesData;
    
    if (genre) {
        filtered = filtered.filter(s => s.genre === genre);
    }
    
    if (length) {
        filtered = filtered.filter(s => s.length === length);
    }
    
    renderStories(filtered);
}

// 清除筛选
function clearFilters() {
    const genreSelect = document.getElementById('filter-genre');
    const lengthSelect = document.getElementById('filter-length');
    
    if (genreSelect) genreSelect.value = '';
    if (lengthSelect) lengthSelect.value = '';
    
    renderStories(storiesData);
}

// 渲染热门故事
function renderPopularStories() {
    const container = document.getElementById('popular-stories');
    if (!container) return;
    
    const popular = [...storiesData]
        .sort((a, b) => b.views - a.views)
        .slice(0, 3);
    
    container.innerHTML = popular.map((story, index) => `
        <div class="popular-story-item" onclick="viewStory(${story.id})">
            <span class="popular-rank">${index + 1}</span>
            <div class="popular-info">
                <h5>${story.title}</h5>
                <span>${formatNumber(story.views)} leitura</span>
            </div>
        </div>
    `).join('');
}

// 初始化
document.addEventListener('DOMContentLoaded', () => {
    renderStories(storiesData);
    renderPopularStories();
});